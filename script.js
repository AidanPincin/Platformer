function breakDown(string){
    const objs = []
    for (let i=0; i<string.length; i++){
        let cols = i
        let rows = 0
        while (cols>69){
            cols -= 70
            rows += 1
        }
        if(string.slice(i,i+1) != '_'){
            objs.push(new classes[string.slice(i,i+1)](cols*20,rows*20))
        }
    }
    return objs
}
