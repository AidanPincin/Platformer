class Obj{
    constructor(x,y,width,height,color){
        this.x = x
        this.y = y
        this.width = width
        this.color = color
        this.height = height
    }
    draw(){
        ctx.fillStyle = this.color
        ctx.fillRect(this.x,this.y,this.width,this.height)
    }
}
class Player{
    constructor(){
        this.keys = ['w','a','d','ArrowUp','ArrowRight','ArrowLeft']
        this.actions = ['up','left','right','up','right','left']
        this.speed = 5
        this.x = 400
        this.y = 760
        this.time = 0
        this.speedY = 0
        this.on = undefined
        this.gravity = 2
        this.size = 20
        this.jump = false
    }
    draw(){
        this.y += this.speedY
        if(this.right == true){
            this.x += this.speed
        }
        if(this.left == true){
            this.x -= this.speed
        }
        this.detectCollision()
        if(this.up == true && this.jump == false){
            this.speedY = -10
            this.time = 1/60
            this.jump = true
        }
        if(this.time != 0){
            this.time += 1/60
            this.speedY += this.time*this.gravity
        }
        if(this.y>=780-this.size){
            this.speedY = -0.01
            this.time = 0
            this.jump = false
            this.y = 780-this.size
        }
        if(this.x<0){
            this.x=0
        }
        if(this.x>1400-this.size){
            this.x=1380
        }
        ctx.fillStyle = '#00ff00'
        ctx.fillRect(this.x,this.y,this.size,this.size)
    }
    keyPress(e,bln){
        this[this.actions[this.keys.findIndex(k => k == e.key)]] = bln
    }
    detectCollision(){
        let onTop = false
        for (let i=0; i<objects.length; i++){
            const {x:x, y:y, width:w, height:h, color:c} = objects[i]
            if(this.x<x+w && this.x+this.size>x && this.y<y+h && this.y+this.size>y){
                if(c == '#007d00'){
                    const x_dist = ((this.x+this.size/2)-(x+w/2))/(w/(w+h))
                    const y_dist = ((this.y+this.size/2)-(y+h/2))/(h/(w+h))
                    if(Math.abs(x_dist)>Math.abs(y_dist)){
                        if(x_dist<0){
                            this.x = x-this.size
                        }
                        else{
                            this.x = x+w
                        }
                    }
                    else{
                        if(y_dist<0){
                            this.y = y-this.size
                            onTop = true
                        }
                        else{
                            this.y = y+h
                            this.speedY = 0
                        }
                    }
                }
                if(c == '#ff0000'){
                    player = new Player()
                }
                if(c == '#0000ff'){
                    lvl += 1
                    data.setItem('lvl',lvl)
                    player = new Player()
                    objects = breakDown(lvls[lvl])
                }
                if(c == '#000000'){
                    this.y = y-20
                    this.speedY = objects[i].speed
                    this.time = 1/60
                    this.jump = false
                }
            }
        }
        if(onTop==true){
            this.time = 0
            this.speedY = 0
            this.jump = false
        }
        else{
            if(this.time==0 && this.y<780-this.size){
                this.time += 1/60
            }
        }
    }
}
